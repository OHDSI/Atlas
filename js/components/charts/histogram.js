define([
	'knockout',
  'providers/Chart',
  'providers/Component',
  'atlascharts',
], function (
  ko,
  Chart,
  Component,
  atlascharts,
) {
  class Histogram extends Chart {
    static get name() {
      return 'histogram';
    }

    constructor(params) {
      super(params);
      this.renderer = new atlascharts.histogram();
    }

  }

  return Component.build(Histogram);
});
